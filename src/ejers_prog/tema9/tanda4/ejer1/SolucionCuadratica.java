package ejers_prog.tema9.tanda4.ejer1;

public class SolucionCuadratica {
	private double s1;
	private double s2;
	
	public SolucionCuadratica(double s1, double s2) {
		super();
		this.s1 = s1;
		this.s2 = s2;
	}

	public double getS1() {
		return s1;
	}

	public double getS2() {
		return s2;
	}
	
	
	
	
}
